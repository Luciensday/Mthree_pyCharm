# this is a program that take input (marks)
# it will print pass if the marks is more than 35 fails if it's less than 35 pass
marks = eval(input("Enter the marks: "))
# conditional statement
if marks < 35:
    print("-----------------------")
    print("Fail")
    print("-----------------------")
else:
    print("-----------------------")
    print("PASS")
    print("-----------------------")
print("this is completes the program")
